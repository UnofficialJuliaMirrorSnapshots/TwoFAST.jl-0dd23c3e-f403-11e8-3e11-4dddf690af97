      if(qreal(x).lt.0) w=1-x
      y=log((((((((((((((((p15/(w+q15)+p14)/(w+q14)
     &  +p13)/(w+q13)+p12)/(w+q12)+p11)/(w+q11)
     &  +p10)/(w+q10)+p9)/(w+q9)+p8)/(w+q8)
