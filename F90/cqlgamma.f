     &  p1= 9.8197319346551593835492039252950643250q-04,
     &  p2= 3.6452833584596387259416230162968814334q-02,
     &  p3= 8.4440218943313053279736838769169637475q-01,
     &  p4= 1.2799395416392675659501629085367313130q+01,
