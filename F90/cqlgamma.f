     &  +p3)/(w+q3)+p2)/(w+q2)+p1)/(w+q1)+p0)/w+pr)
     &  +(w-0.5q0)*log(w+pv)-w
      if(qreal(x).lt.0) y=log(pi/sin(pi*x))-y
      r=0.5q0-qimag(y)/(2*pi)
      s=qint(r)-1
      cqlgamma=y+cmplx(0.0q0,(2*pi)*(qint(r-s)+s))
      end
