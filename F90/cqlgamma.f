      implicit real*16 (o-s)
      parameter(
     &  pi= 3.1415926535897932384626433832795028842q+00,
     &  pv= 1.7073384106072256336552635469768405988q+01,
     &  pr= 9.6430280376084227593136665415665434890q-08,
